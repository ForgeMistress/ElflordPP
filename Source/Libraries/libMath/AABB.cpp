bool Intersects(const AABB_2D& lhs, const AABB_2D& rhs)
{
	return
		!(rhs._left > lhs._right
		|| rhs._right < lhs._left
		|| rhs._top < lhs._bottom
		|| rhs._bottom > lhs._top);
}

